"""# The Ensemble Architecture (STGNN) #"""

# =====================================================================
# STEP 3.1: Define the Upgraded GAT + LSTM Architecture (FIXED)
# =====================================================================
import torch
import torch.nn as nn
import torch.nn.functional as F
from torch_geometric.nn import GATConv

class TrafficGAT_LSTM(nn.Module):
    def __init__(self, in_channels, hidden_dim, seq_len_out, num_heads=4):
        super(TrafficGAT_LSTM, self).__init__()
        self.seq_len_out = seq_len_out

        # 1. SPATIAL LAYER: Graph Attention Network (GAT)
        self.gat = GATConv(in_channels, hidden_dim // num_heads, heads=num_heads, concat=True)

        # 2. TEMPORAL LAYER: LSTM
        self.lstm = nn.LSTM(input_size=hidden_dim, hidden_size=hidden_dim, batch_first=True)

        # 3. REGRESSION HEAD: Output Predictions
        self.regressor = nn.Linear(hidden_dim, seq_len_out)

    def forward(self, x, edge_index):
        # x shape: [Batch, Time, Nodes, Features]
        batch_size, seq_len, num_nodes, num_features = x.size()

        # --- THE FIX: Create a 2D Batched Edge Index ---
        # Flatten the batch of graphs into one giant disconnected 2D graph
        # so GATConv can calculate attention without crashing.
        batched_edges = []
        for i in range(batch_size):
            offset = i * num_nodes
            batched_edges.append(edge_index + offset)
        batched_edge_index = torch.cat(batched_edges, dim=1)

        # --- PHASE 1: Dynamic Spatial Attention (GAT) ---
        gat_out = []
        for t in range(seq_len):
            x_t = x[:, t, :, :] # Extract time step: [Batch, Nodes, Features]

            # Flatten Batch and Nodes into 2D: [Batch * Nodes, Features]
            x_t_flat = x_t.reshape(batch_size * num_nodes, num_features)

            # Push through GAT
            out_t = self.gat(x_t_flat, batched_edge_index)
            out_t = F.relu(out_t)

            # Fold back into 3D: [Batch, Nodes, Hidden_Dim]
            out_t = out_t.view(batch_size, num_nodes, -1)
            gat_out.append(out_t)

        gat_out = torch.stack(gat_out, dim=1)

        # --- PHASE 2: Temporal Processing (LSTM) ---
        lstm_in = gat_out.view(batch_size * num_nodes, seq_len, -1)
        lstm_out, _ = self.lstm(lstm_in)
        last_hidden_state = lstm_out[:, -1, :]

        # --- PHASE 3: Regression ---
        predictions = self.regressor(last_hidden_state)
        predictions = predictions.view(batch_size, num_nodes, self.seq_len_out)
        predictions = predictions.permute(0, 2, 1)

        return predictions

