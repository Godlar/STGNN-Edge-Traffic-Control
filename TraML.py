"""# The Training and Validation Loop #"""

# =====================================================================
# STEP 4.1: Setup Optimizer and Loss Function
# =====================================================================
import torch.optim as optim
import time

# Mean Squared Error is the standard for regression (predicting continuous numbers)
criterion = nn.MSELoss()

# The Adam optimizer is excellent at navigating the complex gradients of LSTMs
# A learning rate of 0.001 is a safe, stable starting point
optimizer = optim.Adam(model.parameters(), lr=0.001)

# =====================================================================
# STEP 4.2: Execute the Training Loop
# =====================================================================
# We will do 10 passes over the entire dataset to verify the network is learning
num_epochs = 10

print(f"Igniting the Engine on {device}...\n")

for epoch in range(num_epochs):
    start_time = time.time()

    # --- PHASE 1: TRAINING ---
    model.train()
    train_loss = 0.0

    for batch_x, batch_y in train_loader:
        # Move the data batches to the GPU
        batch_x = batch_x.to(device)
        batch_y = batch_y.to(device)

        # 1. Forward Pass: Push data through GCN -> LSTM -> Linear layers
        predictions = model(batch_x, edge_index)

        # 2. Calculate Loss: How far off were the traffic predictions?
        loss = criterion(predictions, batch_y)

        # 3. Backward Pass: Clear old math, calculate the new gradients
        optimizer.zero_grad()
        loss.backward()

        # 4. Update Weights: Nudge the network to be slightly more accurate
        optimizer.step()

        train_loss += loss.item()

    avg_train_loss = train_loss / len(train_loader)

    # --- PHASE 2: VALIDATION ---
    # We test on unseen data to ensure the model isn't just memorizing the training set
    model.eval()
    val_loss = 0.0

    with torch.no_grad(): # Turn off gradient tracking to save memory and speed up testing
        for batch_x, batch_y in val_loader:
            batch_x = batch_x.to(device)
            batch_y = batch_y.to(device)

            predictions = model(batch_x, edge_index)
            loss = criterion(predictions, batch_y)
            val_loss += loss.item()

    avg_val_loss = val_loss / len(val_loader)
    epoch_time = time.time() - start_time

    print(f"Epoch [{epoch+1:02d}/{num_epochs}] | Time: {epoch_time:.1f}s | Train Loss: {avg_train_loss:.4f} | Val Loss: {avg_val_loss:.4f}")

print("\n✅ Initial Training Complete!")
print("If the Validation Loss went down, the ensemble is officially learning traffic patterns.")

