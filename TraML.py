# 5. Define Sliding Window Dataset
class TrafficDataset(Dataset):
    def __init__(self, data, seq_len_in, seq_len_out):
        self.data = data
        self.seq_len_in = seq_len_in
        self.seq_len_out = seq_len_out

    def __len__(self):
        return len(self.data) - self.seq_len_in - self.seq_len_out + 1

    def __getitem__(self, idx):
        x = self.data[idx : idx + self.seq_len_in]
        y = self.data[idx + self.seq_len_in : idx + self.seq_len_in + self.seq_len_out]
        return x, y.squeeze(-1)

